    private void datoParticularKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_datoParticularKeyPressed
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            if (tipoVehiculo.getSelectedItem().toString().equals("Elegir opción...")) {
                labelError.setVisible(true);
            } else if (lugarTxt.getText().toString().equals("Elegir...")) {
                JOptionPane.showMessageDialog(vistaIngreso, "Elija un lugar para el vehículo");
            } else if (patente.getText().isEmpty()) {
                JOptionPane.showMessageDialog(vistaIngreso, "Ingrese una patente");
            } else if (datoParticular.getText().isEmpty()) {
                JOptionPane.showMessageDialog(vistaIngreso, "Ingrese el dato particular");
            } else {
                ControladorIngreso.ingresoVehiculo(patente.getText(), Integer.parseInt(lugarTxt.getText()), tipoVehiculo.getSelectedItem().toString(), datoParticular.getText());
                ControladorListado.actualizarLista();
                ControladorLugares.ActualizarGrilla();
                VaciarTextFields();
            }
        }
    }//GEN-LAST:event_datoParticularKeyPressed

