    private void tipoVehiculoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tipoVehiculoActionPerformed

        switch (tipoVehiculo.getSelectedItem().toString()) {
            case "Auto":
                labelDatoParticular.setText("Cantidad de Puertas");
                break;
            case "Moto":
                labelDatoParticular.setText("Cilindradas");
                break;
            case "Camioneta":
                labelDatoParticular.setText("Cabina");
                break;
        }
        datoParticular.setVisible(true);
        labelDatoParticular.setVisible(true);
    }//GEN-LAST:event_tipoVehiculoActionPerformed

